void AddRoundKey( unsigned char *a , unsigned char *Key )  {  //  轮密钥加  
	for( int i = 0 ; i < 16 ; i ++ )
		a[i] ^= Key[i] ;
}

void AesEncrypt(unsigned char *blk, unsigned char *key, int Nr){
	int round;
	AddRoundKey(blk,key);
	for(round=1;round<=(Nr-1);round++)	
	{
		SubBytes(blk);		
		ShiftRows(blk);		
		MixColumns(blk);	
		AddRoundKey(blk,key);
	}
	SubBytes(blk);
	ShiftRows(blk);
	AddRoundKey(blk,key);
}

void ScheduleKey(unsigned char *inkey,unsigned char *outkey, int Nk, int Nr)
{
	unsigned char temp[4],t;
	int m,n;
	
	for(n=0;n<(4*Nk);n++){
		outkey[n]=inkey[n];
	}
	n=Nk;
	
	unsigned char RC[10];
	RC[0]=1;
	for(int i=1;i<10;i++){
		RC[i]=Mul(0x02,RC[i-1]);
	}
	
	while(n<(4*(Nr+1))) 
	{
		for(m=0;m<4;m++) 
			temp[m]=outkey[(4*(n-1))+m];	
		if(n%Nk==0)
		{
			t=temp[0];temp[0]=temp[1];temp[1]=temp[2];temp[2]=temp[3];temp[3]=t;
			for(m=0;m<4;m++){
				temp[m]=S[temp[m]];
			}
			temp[0]^=RC[(n/Nk)-1];
		}
		for(m=0;m<4;m++){
			outkey[(4*n)+m]=outkey[(4*(n-Nk))+m]^temp[m];
		}
		++n;
	}
}

void AesDecrypt(unsigned char *blk, unsigned char *key, int Nr)//解密方法一 替换步骤顺序和加密不一样
{
	AddRoundKey(blk, key);
	for (int round = Nr - 1; round > 0; round--) {
		InvShiftRows(blk);
		InvSubBytes(blk);
		AddRoundKey(blk, key);
		InvMixColumns(blk);
	}
	InvShiftRows(blk);
	InvSubBytes(blk);
	AddRoundKey(blk, key);
}

